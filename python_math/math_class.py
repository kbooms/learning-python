import math

x = 9.1

# calls the value of pi
print(math.pi)
# e is the exponential constant
print(math.e)
# sqrt() returns square roots of the contained value
result = math.sqrt(x)
print(result)

# ceil() calculates the smallest integer no smaller than the contained number
result = math.ceil(x)
print(result)

# floor() calculates the largest integer no larger than the contained number
result = math.floor(x)
print(result)